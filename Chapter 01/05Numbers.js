let score = 50;
console.log(score);
console.log(typeof score);

// MATHMATICAL OPERATORS

let result  = score/2;
let result2 = score % 2;
console.log(result);
console.log(result2);


let result3 = score * 2 + (4 * 3) - 8 / 2 % 4;
console.log(result3);

// Concatination of Numbers
let resultline = 'My Total Score is = ' + score;
console.log(resultline);


// Loose Eqality (==) vs Strict Equality Operator (===)
let age = 23;
console.log(age == "22");
console.log(age === "22");
